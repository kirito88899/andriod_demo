package com.example.myapplication;

public class HomeList {
    String ct;
    String tto;


    public HomeList(String ct, String tto) {
        this.ct = ct;
        this.tto = tto;
    }

    public String getCt() {
        return ct;
    }

    public void setCt(String ct) {
        this.ct = ct;
    }

    public String getTto() {
        return tto;
    }

    public void setTto(String tto) {
        this.tto = tto;
    }
}
